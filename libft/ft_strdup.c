#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*res;
	size_t	len;

	len = ft_strlen(s);
	res = (char *)malloc(len * sizeof(char) + 1);
	if (!res)
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
